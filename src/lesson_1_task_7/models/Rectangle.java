package lesson_1_task_7.models;

public class Rectangle {
    public int height;
    public int width;
    public char symbol;
    public int shift;

    public Rectangle(int valueHeight, int valueWidth, char valueSymbol, int valueShift) {
        height = valueHeight;
        width = valueWidth;
        symbol = valueSymbol;
        shift = valueShift;
    }
}
